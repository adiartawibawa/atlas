        $this->data['currentCategory'] = $currentCategory;
        $this->data['settings'] = Setting::getSettings()[$currentCategory];
        $this->data['categories'] = Setting::getCategories();

        return Inertia::render('Admin/Setting/Index', ['settings' => $this->data]);
    }

